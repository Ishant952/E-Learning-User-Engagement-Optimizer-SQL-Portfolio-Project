CREATE TABLE courses (
    institution VARCHAR(50),
    course_number VARCHAR(50),
    launch_date DATE,
    course_title TEXT,
    course_subject VARCHAR(100),
    year INT,
    participants_course_content_accessed INT,
    pct_played_video FLOAT,
    pct_posted_in_forum FLOAT,
    pct_certified FLOAT,
    median_hours_for_certification FLOAT,
    median_age FLOAT,
    pct_bachelors_or_higher FLOAT
);
